function imagefun() {
    var Image_Id = document.getElementById('loveimage');
    if (Image_Id.src.match("images/love.png")) {
        Image_Id.src = "images/lovered.png";
    }
    else {
        Image_Id.src = "images/love.png";
    }
}  

